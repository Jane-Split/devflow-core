/**
 * Test Generator
 * Generates unit, integration, and E2E tests from interface specs
 */

import { Logger } from '../utils/logger.js';

const logger = new Logger('TestGenerator');

/**
 * Test types
 */
export const TestType = {
  UNIT: 'unit',
  INTEGRATION: 'integration',
  E2E: 'e2e',
};

/**
 * Test framework types
 */
export const TestFramework = {
  JEST: 'jest',
  VITEST: 'vitest',
  PLAYWRIGHT: 'playwright',
  CYPRESS: 'cypress',
  PYTEST: 'pytest',
  JUNIT: 'junit',
};

/**
 * Test Generator
 */
export class TestGenerator {
  /**
   * @param {Object} config - Configuration
   */
  constructor(config = {}) {
    this.config = config;
    this.framework = config.framework || TestFramework.JEST;
  }

  /**
   * Generate unit tests from interface spec
   * @param {Object} interfaceSpec - Interface specification
   * @returns {Array} Array of test files
   */
  generateUnitTests(interfaceSpec) {
    logger.debug(`Generating unit tests for ${interfaceSpec.name}`);

    const tests = [];

    // Generate main test case
    tests.push(this._generateMainTestCase(interfaceSpec));

    // Generate boundary tests
    tests.push(...this._generateBoundaryTests(interfaceSpec));

    // Generate error handling tests
    tests.push(...this._generateErrorTests(interfaceSpec));

    return tests;
  }

  /**
   * Generate main test case
   */
  _generateMainTestCase(spec) {
    const testName = `${spec.name}.should_work_correctly`;

    return {
      name: testName,
      type: TestType.UNIT,
      framework: this.framework,
      content: this._generateJestTest(`
describe('${spec.name}', () => {
  describe('main functionality', () => {
    it('should ${spec.description || 'work as expected'}', () => {
      // Given
      ${this._generateTestSetup(spec)}

      // When
      ${this._generateTestExecution(spec)}

      // Then
      ${this._generateAssertions(spec)}
    });
  });
});
      `),
      filePath: this._getTestFilePath(spec.name, 'test'),
    };
  }

  /**
   * Generate boundary tests
   */
  _generateBoundaryTests(spec) {
    const tests = [];
    const params = spec.parameters || spec.inputs || [];

    // Empty input test
    tests.push({
      name: `${spec.name}.should_handle_empty_input`,
      type: TestType.UNIT,
      framework: this.framework,
      content: this._generateJestTest(`
describe('${spec.name}', () => {
  describe('edge cases', () => {
    it('should handle empty input', () => {
      // Given
      const input = ${this._generateEmptyInput(params)};

      // When
      ${this._generateTestExecution(spec, 'input')}

      // Then
      ${this._generateEdgeCaseAssertion(spec)}
    });

    it('should handle wrong input type', () => {
      // Given
      const input = 'invalid';

      // When & Then
      expect(() => ${this._generateFunctionCall(spec, 'input')}).toThrow();
    });
  });
});
      `),
      filePath: this._getTestFilePath(spec.name, 'edge-cases.test'),
    });

    return tests;
  }

  /**
   * Generate error handling tests
   */
  _generateErrorTests(spec) {
    return [
      {
        name: `${spec.name}.should_handle_errors`,
        type: TestType.UNIT,
        framework: this.framework,
        content: this._generateJestTest(`
describe('${spec.name}', () => {
  describe('error handling', () => {
    it('should throw on invalid input', async () => {
      // Given
      const invalidInput = null;

      // When & Then
      await expect(${this._generateFunctionCall(spec, 'invalidInput')}).rejects.toThrow();
    });

    it('should handle async errors gracefully', async () => {
      // Given
      jest.spyOn(console, 'error').mockImplementation(() => {});

      // When
      try {
        await ${this._generateAsyncFunctionCall(spec, 'throw new Error("test")')};
      } catch (error) {
        // Then
        expect(error.message).toBe('test');
      }
    });
  });
});
      `),
        filePath: this._getTestFilePath(spec.name, 'errors.test'),
      },
    ];
  }

  /**
   * Generate Jest test file
   */
  _generateJestTest(content) {
    return `/**
 * Auto-generated test file
 * @generated by DevFlow
 */

${content}
    `.trim();
  }

  /**
   * Generate test setup
   */
  _generateTestSetup(spec) {
    const params = spec.parameters || spec.inputs || [];
    if (params.length === 0) {
      return '// No setup needed';
    }

    return params
      .map(p => {
        const mock = this._generateMock(p);
        return `const ${p.name} = ${mock};`;
      })
      .join('\n');
  }

  /**
   * Generate mock data for parameter
   */
  _generateMock(param) {
    switch (param.type) {
      case 'string':
        return `'${param.example || 'test-value'}'`;
      case 'number':
        return param.example || 42;
      case 'boolean':
        return param.example !== undefined ? param.example : true;
      case 'array':
        return `['${param.example || 'item'}']`;
      case 'object':
        return JSON.stringify(param.example || { id: 1, name: 'test' });
      default:
        return 'null';
    }
  }

  /**
   * Generate test execution
   */
  _generateTestExecution(spec, inputVar = null) {
    if (spec.async) {
      return `const result = await ${spec.name}(${inputVar || 'input'});`;
    }
    return `const result = ${spec.name}(${inputVar || 'input'});`;
  }

  /**
   * Generate assertions
   */
  _generateAssertions(spec) {
    if (spec.expectedOutput) {
      return `expect(result).toEqual(${JSON.stringify(spec.expectedOutput)});`;
    }
    if (spec.expectedType) {
      return `expect(result).toBeDefined();`;
    }
    return 'expect(result).toBeTruthy();';
  }

  /**
   * Generate empty input
   */
  _generateEmptyInput(params) {
    if (params.length === 0) {
      return 'undefined';
    }

    const emptyParams = params
      .map(p => {
        if (p.required) {
          return null;
        }
        return `${p.name}: null`;
      })
      .filter(p => p !== null);

    return emptyParams.length > 0 ? `{ ${emptyParams.join(', ')} }` : 'undefined';
  }

  /**
   * Generate edge case assertion
   */
  _generateEdgeCaseAssertion(spec) {
    if (spec.handlesEmpty !== false) {
      return `expect(result).toBeDefined();`;
    }
    return `expect(result).toBeNull();`;
  }

  /**
   * Generate function call
   */
  _generateFunctionCall(spec, varName) {
    return `${spec.name}(${varName})`;
  }

  /**
   * Generate async function call
   */
  _generateAsyncFunctionCall(spec, errorCode) {
    return `${spec.name}().catch(e => ${errorCode});`;
  }

  /**
   * Get test file path
   */
  _getTestFilePath(name, suffix = 'test') {
    const ext = this.framework === TestFramework.PYTEST ? '.py' : '.test.js';
    const baseName = name
      .replace(/([A-Z])/g, '-$1')
      .toLowerCase()
      .replace(/^-/, '');
    return `${baseName}.${suffix}${ext}`;
  }

  /**
   * Generate Playwright E2E test
   * @param {Object} pageSpec - Page specification
   * @returns {Object} E2E test
   */
  generateE2ETest(pageSpec) {
    logger.debug(`Generating E2E test for ${pageSpec.name}`);

    const steps = pageSpec.steps || [];
    const assertions = pageSpec.assertions || [];

    return {
      name: `${pageSpec.name}.e2e.spec.ts`,
      type: TestType.E2E,
      framework: TestFramework.PLAYWRIGHT,
      content: this._generatePlaywrightTest(pageSpec, steps, assertions),
      filePath: `e2e/${pageSpec.name.toLowerCase()}.spec.ts`,
    };
  }

  /**
   * Generate Playwright test file
   */
  _generatePlaywrightTest(spec, steps, assertions) {
    const testSteps = steps
      .map(step => {
        switch (step.action) {
          case 'navigate':
            return `    await page.goto('${step.url}');`;
          case 'click':
            return `    await page.click('${step.selector}');`;
          case 'fill':
            return `    await page.fill('${step.selector}', '${step.value}');`;
          case 'type':
            return `    await page.type('${step.selector}', '${step.value}');`;
          case 'wait':
            return `    await page.waitForSelector('${step.selector}');`;
          case 'hover':
            return `    await page.hover('${step.selector}');`;
          case 'screenshot':
            return `    await page.screenshot({ path: '${step.path || 'screenshot.png'}' });`;
          default:
            return `    // Unknown action: ${step.action}`;
        }
      })
      .join('\n');

    const testAssertions = assertions
      .map(a => {
        switch (a.type) {
          case 'visible':
            return `    await expect(page.locator('${a.selector}')).toBeVisible();`;
          case 'text':
            return `    await expect(page.locator('${a.selector}')).toHaveText('${a.expected}');`;
          case 'count':
            return `    await expect(page.locator('${a.selector}')).toHaveCount(${a.count});`;
          case 'url':
            return `    await expect(page).toHaveURL('${a.url}');`;
          default:
            return `    // Unknown assertion: ${a.type}`;
        }
      })
      .join('\n');

    return `/**
 * Auto-generated E2E test
 * @generated by DevFlow
 */

import { test, expect } from '@playwright/test';

test.describe('${spec.name}', () => {
  test('${spec.description || 'should work as expected'}', async ({ page }) => {
    // Setup
    ${spec.setup || ''}

    // Actions
${testSteps}

    // Assertions
${testAssertions}

    // Cleanup
    ${spec.cleanup || ''}
  });
});
`;
  }

  /**
   * Generate integration test
   * @param {Object} apiSpec - API specification
   * @returns {Object} Integration test
   */
  generateIntegrationTest(apiSpec) {
    logger.debug(`Generating integration test for ${apiSpec.endpoint}`);

    return {
      name: `${apiSpec.name}.integration.test.ts`,
      type: TestType.INTEGRATION,
      framework: this.framework,
      content: this._generateIntegrationTestContent(apiSpec),
      filePath: `integration/${apiSpec.name.toLowerCase()}.test.ts`,
    };
  }

  /**
   * Generate integration test content
   */
  _generateIntegrationTestContent(apiSpec) {
    return `/**
 * Auto-generated integration test
 * @generated by DevFlow
 */

describe('${apiSpec.name} API', () => {
  const BASE_URL = process.env.API_BASE_URL || 'http://localhost:3000';

  describe('${apiSpec.endpoint}', () => {
    describe('${apiSpec.method} request', () => {
      it('should return expected response', async () => {
        // Given
        const request = {
          ${this._generateRequestBody(apiSpec.body)}
        };

        // When
        const response = await fetch(\`\${BASE_URL}${apiSpec.endpoint}\`, {
          method: '${apiSpec.method}',
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify(request),
        });

        // Then
        expect(response.status).toBe(${apiSpec.expectedStatus || 200});
        
        const data = await response.json();
        expect(data).toHaveProperty('${apiSpec.responseField || 'id'}');
      });

      it('should handle authentication', async () => {
        // Given
        const token = 'test-token';

        // When
        const response = await fetch(\`\${BASE_URL}${apiSpec.endpoint}\`, {
          method: '${apiSpec.method}',
          headers: {
            'Authorization': \`Bearer \${token}\`,
          },
        });

        // Then
        expect(response.status).toBeGreaterThanOrEqual(200);
        expect(response.status).toBeLessThan(300);
      });

      it('should handle errors gracefully', async () => {
        // Given
        const invalidRequest = {
          ${this._generateInvalidRequest(apiSpec.body)}
        };

        // When
        const response = await fetch(\`\${BASE_URL}${apiSpec.endpoint}\`, {
          method: '${apiSpec.method}',
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify(invalidRequest),
        });

        // Then
        expect(response.status).toBeGreaterThanOrEqual(400);
      });
    });
  });
});
`;
  }

  /**
   * Generate request body
   */
  _generateRequestBody(body) {
    if (!body) {
      return '// No body required';
    }
    return Object.entries(body)
      .map(([key, value]) => {
        return `${key}: '${value || 'test-value'}'`;
      })
      .join(',\n          ');
  }

  /**
   * Generate invalid request
   */
  _generateInvalidRequest(body) {
    if (!body) {
      return 'invalid: true';
    }
    const key = Object.keys(body)[0];
    return `${key}: null`;
  }
}

/**
 * Create test generator
 */
export function createTestGenerator(config = {}) {
  return new TestGenerator(config);
}
